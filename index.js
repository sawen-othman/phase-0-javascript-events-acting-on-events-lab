function moveDodgerLeft(){
    const dodger = document.getElementById("dodger");
    dodger.style.bottom = "0px";
    dodger.style.left = "0px";
}

function moveDodgerRight(){
    const dodger = document.getElementById("dodger");
    dodger.style.bottom = "0px";
    dodger.style.left = "100px";
}